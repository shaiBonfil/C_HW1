#include "myMath.h"

#define e 2.7182818285

double Exponent(int x){
    double res =1;
    int i;
    for (i = 0; i < x; add(i, 1))
    {
       res = mul(res, e);
    }
    return res;
    
}


double Power(double x , int y)
{
    double res = 1;
    int i;
    for (i = 0; i < y; add(i, 1))
    {
        res = mul(res, x);
    }
    return res; 
}